// ... rest of the JavaScript code ...
// (displayNames, handleSearch, and event listeners)

const namesGrid = document.getElementById('names-grid');
const searchInput = document.getElementById('name-search');

// Function to display the names on the page
function displayNames(names) {
    namesGrid.innerHTML = ''; // Clear previous names
    names.forEach(name => {
        const card = document.createElement('div');
        card.classList.add('name-card');
        card.innerHTML = `
            <h3 class="name-arabic">${name.arabic}</h3>
            <p class="name-english">${name.english}</p>
            <p class="meaning-english">${name.meaningEnglish}</p>
            <p class="meaning-urdu">${name.meaningUrdu}</p>
        `;
        namesGrid.appendChild(card);
    });
}

// Function to handle the search
function handleSearch() {
    const searchTerm = searchInput.value.toLowerCase().replace(/-/g, '').replace(/ /g, '');
    
    const filteredNames = asmaUlHusna.filter(name => {
        // Normalize the names for searching
        const normalizedEnglish = name.english.toLowerCase().replace(/-/g, '').replace(/ /g, '');
        const normalizedMeaningEnglish = name.meaningEnglish.toLowerCase().replace(/-/g, '').replace(/ /g, '');
        const normalizedMeaningUrdu = name.meaningUrdu.toLowerCase(); // Urdu mein hyphen nahi hote

        return normalizedEnglish.includes(searchTerm) ||
               normalizedMeaningEnglish.includes(searchTerm) ||
               normalizedMeaningUrdu.includes(searchTerm);
    });
    displayNames(filteredNames);
}
// Event listener for the search input
searchInput.addEventListener('keyup', handleSearch);

// Initial display of all names when the page loads
document.addEventListener('DOMContentLoaded', () => {
    displayNames(asmaUlHusna);
});
